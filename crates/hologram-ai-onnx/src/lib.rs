/// Main ONNX compiler interface.
///
/// Provides high-level API for compiling ONNX models to .holo format with
/// full ISA optimization support.
///
/// # Examples
///
/// ```ignore
/// use hologram_ai_onnx::{OnnxCompiler, OnnxConfig};
/// use std::fs;
///
/// // Use default configuration
/// let compiler = OnnxCompiler::new();
///
/// let onnx_bytes = fs::read("model.onnx")?;
/// let (holo_bytes, weight_bytes) = compiler.compile(&onnx_bytes)?;
///
/// fs::write("model.holo", holo_bytes)?;
/// if !weight_bytes.is_empty() {
///     fs::write("model.weights", weight_bytes)?;
/// }
/// ```
pub struct OnnxCompiler {
    config: OnnxConfig,
}

impl OnnxCompiler {
    /// Create a new compiler with default configuration.
    ///
    /// Default settings:
    /// - Weight threshold: 4096 bytes
    /// - Partitioning: disabled
    /// - Partition size: 500 nodes
    /// - Conv2D decomposition: enabled
    /// - Pooling decomposition: enabled
    /// - Memory budget: unlimited
    pub fn new() -> Self {
        Self {
            config: OnnxConfig::default(),
        }
    }

    /// Create a new compiler with custom configuration.
    pub fn with_config(config: OnnxConfig) -> Self {
        Self { config }
    }

    /// Compile ONNX model to .holo format.
    ///
    /// This method performs the complete compilation pipeline:
    /// 1. Parse ONNX protobuf
    /// 2. Translate to IR with symbolic shapes
    /// 3. Apply hologram-ir graph building
    /// 4. Serialize to .holo format (weights embedded)
    ///
    /// # Arguments
    ///
    /// * `onnx_bytes` - Raw ONNX model bytes (protobuf format)
    ///
    /// # Returns
    ///
    /// A tuple of `(holo_bytes, weight_bytes)`:
    /// - `holo_bytes`: Serialized .holo bytes (includes embedded weights)
    /// - `weight_bytes`: Optional external weights copy for separate storage (may be empty)
    #[tracing::instrument(
        name = "compile_onnx",
        skip_all,
        fields(onnx_size = onnx_bytes.len())
    )]
    pub fn compile(&self, onnx_bytes: &[u8]) -> Result<(Vec<u8>, Vec<u8>)> {
        // Phase 1: Parse and validate ONNX model
        let (model, opset_version) = {
            let _span = tracing::info_span!("parse_onnx").entered();
            let model = parse_model(onnx_bytes)?;
            validate_model(&model)?;
            let opset_version = core::extract_opset_version(&model);
            (model, opset_version)
        };
        tracing::info!(opset_version = opset_version, "Parsed ONNX model");

        // Get the graph
        let graph = model
            .graph
            .as_ref()
            .ok_or_else(|| OnnxError::InvalidModel("Model has no graph".into()))?;

        // Check if partitioning is needed
        if self.config.enable_partitioning && graph.node.len() > self.config.partition_size {
            tracing::info!(
                nodes = graph.node.len(),
                "Large graph detected, using partitioning"
            );
            return self.compile_partitioned(onnx_bytes);
        }

        // Phase 2: Translate ONNX → IR with symbolic shapes
        let ir_func = {
            let _span = tracing::info_span!("translate_to_ir", nodes = graph.node.len()).entered();
            if self.config.enable_parallel_execution {
                translate_graph_to_ir_with_groups(graph)?
            } else {
                translate_graph_to_ir(graph)?
            }
        };

        // Phase 3: Compile to BackendPlan using hologram-compiler
        let (mut plan, header) = {
            let _span = tracing::info_span!("compile_ir").entered();
            let backend_type = hologram::BackendType::Cpu;
            self.compile_ir_with_config(&ir_func, backend_type)?
        };
        repair_output_metadata_from_graph(&mut plan, graph);
        dump_plan_if_requested(&plan, "onnx_compile")?;

        // Capture weights for external use without stripping them from the plan.
        let weight_bytes = if plan.constant_data.len() > self.config.weight_threshold {
            tracing::info!(
                weight_bytes = plan.constant_data.len(),
                threshold = self.config.weight_threshold,
                "Capturing weights for external file"
            );
            plan.constant_data.clone()
        } else {
            Vec::new()
        };

        // Phase 4: Serialize to .holo format
        let holo_bytes = {
            let _span = tracing::info_span!("serialize_holo").entered();
            serialize_backend_plan_with_header(&plan, &header)?
        };

        tracing::info!(
            holo_bytes = holo_bytes.len(),
            weight_bytes = weight_bytes.len(),
            "Compilation complete"
        );

        Ok((holo_bytes, weight_bytes))
    }

    /// Compile large model using graph partitioning.
    #[tracing::instrument(
        name = "compile_partitioned",
        skip_all,
        fields(onnx_size = onnx_bytes.len())
    )]
    pub fn compile_partitioned(&self, onnx_bytes: &[u8]) -> Result<(Vec<u8>, Vec<u8>)> {
        // Phase 1: Parse model
        let model = {
            let _span = tracing::info_span!("parse_onnx").entered();
            let model = parse_model(onnx_bytes)?;
            validate_model(&model)?;
            model
        };

        let graph = model
            .graph
            .as_ref()
            .ok_or_else(|| OnnxError::InvalidModel("Model has no graph".into()))?;

        // Phase 2: Analyze partitioning structure
        let partitions = {
            let _span = tracing::info_span!("partition_graph", nodes = graph.node.len()).entered();
            let partitioner = core::GraphPartitioner::new();
            partitioner.partition(graph)?
        };
        tracing::info!(
            nodes = graph.node.len(),
            partitions = partitions.len(),
            "Graph partitioned"
        );

        // Phase 3: Translate to IR
        let ir_func = {
            let _span = tracing::info_span!("translate_to_ir").entered();
            if self.config.enable_parallel_execution {
                translate_graph_to_ir_with_groups(graph)?
            } else {
                translate_graph_to_ir(graph)?
            }
        };

        // Phase 4: Compile to BackendPlan
        let (mut plan, header) = {
            let _span = tracing::info_span!("compile_ir").entered();
            let backend_type = hologram::BackendType::Cpu;
            self.compile_ir_with_config(&ir_func, backend_type)?
        };
        repair_output_metadata_from_graph(&mut plan, graph);
        dump_plan_if_requested(&plan, "onnx_partitioned")?;

        // Capture weights for external use without stripping them from the plan.
        let weight_bytes = if plan.constant_data.len() > self.config.weight_threshold {
            tracing::info!(
                weight_bytes = plan.constant_data.len(),
                "Capturing weights for external file"
            );
            plan.constant_data.clone()
        } else {
            Vec::new()
        };

        // Phase 5: Serialize
        let holo_bytes = {
            let _span = tracing::info_span!("serialize_holo").entered();
            serialize_backend_plan_with_header(&plan, &header)?
        };

        tracing::info!(
            holo_bytes = holo_bytes.len(),
            weight_bytes = weight_bytes.len(),
            "Partitioned compilation complete"
        );

        Ok((holo_bytes, weight_bytes))
    }

    /// Compile ONNX model to a unified bundle (HOLB format).
    ///
    /// If `embedded_files` are configured, this produces a V2 bundle with
    /// embedded sections for vocabulary, tokenizer config, etc.
    #[tracing::instrument(
        name = "compile_to_bundle",
        skip_all,
        fields(onnx_size = onnx_bytes.len())
    )]
    pub fn compile_to_bundle(&self, onnx_bytes: &[u8]) -> Result<Vec<u8>> {
        // Phase 1: Parse and validate ONNX model
        let (model, opset_version) = {
            let _span = tracing::info_span!("parse_onnx").entered();
            let model = parse_model(onnx_bytes)?;
            validate_model(&model)?;
            let opset_version = core::extract_opset_version(&model);
            (model, opset_version)
        };
        tracing::info!(opset_version = opset_version, "Parsed ONNX model");

        // Get the graph
        let graph = model
            .graph
            .as_ref()
            .ok_or_else(|| OnnxError::InvalidModel("Model has no graph".into()))?;

        // Phase 2: Translate ONNX → IR with symbolic shapes
        let ir_func = {
            let _span = tracing::info_span!("translate_to_ir", nodes = graph.node.len()).entered();
            if self.config.enable_parallel_execution {
                translate_graph_to_ir_with_groups(graph)?
            } else {
                translate_graph_to_ir(graph)?
            }
        };

        // Phase 3: Compile to BackendPlan using hologram-compiler
        let (mut plan, header) = {
            let _span = tracing::info_span!("compile_ir").entered();
            let backend_type = hologram::BackendType::Cpu;
            self.compile_ir_with_config(&ir_func, backend_type)?
        };
        repair_output_metadata_from_graph(&mut plan, graph);
        dump_plan_if_requested(&plan, "onnx_bundle")?;

        // Capture weights from plan (they'll be embedded in the bundle).
        let weight_bytes = plan.constant_data.clone();
        tracing::debug!(
            weight_bytes = weight_bytes.len(),
            "Captured weights for bundle"
        );

        // Phase 4: Serialize plan to .holo format.
        let graph_bytes = {
            let _span = tracing::info_span!("serialize_holo").entered();
            serialize_backend_plan_with_header(&plan, &header)?
        };

        // Phase 5: Create unified bundle
        let bundle = {
            let _span = tracing::info_span!(
                "create_bundle",
                embedded_sections = self.config.embedded_files.len()
            )
            .entered();

            let mut writer = core::UnifiedBundleWriter::new();
            writer.set_graph_bytes(graph_bytes);
            writer.set_weights_bytes(weight_bytes);
            writer.add_section(core::sections::LayerHeaderSection {
                header: header.clone(),
            });
            let input_order = Self::external_input_order(graph);
            if !input_order.is_empty() {
                writer.add_section(core::sections::InputOrderSection::new(input_order));
            }

            // Load and embed sections if configured
            for file_config in &self.config.embedded_files {
                let (section_id, content_type, data) = self.load_section_data(file_config)?;
                writer.add_raw_section(&section_id, &content_type, data);
            }

            writer.finish()
        };

        tracing::info!(
            bundle_bytes = bundle.len(),
            v2 = self.config.produces_v2_bundle(),
            "Bundle compilation complete"
        );

        Ok(bundle)
    }

    /// Compile ONNX model to a unified bundle with a base path for resolving relative paths.
    ///
    /// This variant allows specifying a base path for resolving relative paths
    /// in `embedded_files` configuration.
    ///
    /// # Arguments
    ///
    /// * `onnx_bytes` - The ONNX model bytes
    /// * `base_path` - Base path for resolving relative embedded file paths
    ///
    /// # Examples
    ///
    /// ```ignore
    /// use hologram_ai_onnx::{OnnxCompiler, OnnxConfig, EmbeddedFileConfig};
    ///
    /// let config = OnnxConfig::new()
    ///     .with_embedded_file(EmbeddedFileConfig::vocabulary("vocab.txt"));
    ///
    /// let compiler = OnnxCompiler::with_config(config);
    /// let onnx_bytes = std::fs::read("models/bert/model.onnx")?;
    /// let bundle = compiler.compile_to_bundle_with_base_path(&onnx_bytes, "models/bert/")?;
    /// ```
    pub fn compile_to_bundle_with_base_path(
        &self,
        onnx_bytes: &[u8],
        base_path: impl AsRef<std::path::Path>,
    ) -> Result<Vec<u8>> {
        use tracing::{debug, info};

        let base_path = base_path.as_ref();
        info!("Starting ONNX compilation to unified bundle");

        // Step 1: Parse and validate ONNX model
        debug!("Parsing ONNX protobuf");
        let model = parse_model(onnx_bytes)?;
        validate_model(&model)?;
        let opset_version = core::extract_opset_version(&model);
        info!("ONNX opset version: {}", opset_version);

        // Get the graph
        let graph = model
            .graph
            .as_ref()
            .ok_or_else(|| OnnxError::InvalidModel("Model has no graph".into()))?;

        // Step 2: Translate ONNX → IR with symbolic shapes
        debug!("Translating ONNX to IR");
        let ir_func = if self.config.enable_parallel_execution {
            translate_graph_to_ir_with_groups(graph)?
        } else {
            translate_graph_to_ir(graph)?
        };
        info!("IR translation complete");

        // Step 3: Compile to BackendPlan using hologram-compiler
        debug!("Compiling IR to BackendPlan");
        let backend_type = hologram::BackendType::Cpu;
        let (mut plan, header) = self.compile_ir_with_config(&ir_func, backend_type)?;
        repair_output_metadata_from_graph(&mut plan, graph);
        dump_plan_if_requested(&plan, "onnx_bundle")?;

        // Capture weights from plan (they'll be embedded in the bundle).
        let weight_bytes = plan.constant_data.clone();
        info!(
            "Captured {} bytes of weights for bundle",
            weight_bytes.len()
        );

        // Step 4: Serialize plan to .holo format.
        debug!("Serializing BackendPlan");
        let graph_bytes = serialize_backend_plan_with_header(&plan, &header)?;

        // Step 5: Create unified bundle
        debug!("Creating unified bundle");
        let mut writer = core::UnifiedBundleWriter::new();
        writer.set_graph_bytes(graph_bytes);
        writer.set_weights_bytes(weight_bytes);
        writer.add_section(core::sections::LayerHeaderSection {
            header: header.clone(),
        });
        let input_order = Self::external_input_order(graph);
        if !input_order.is_empty() {
            writer.add_section(core::sections::InputOrderSection::new(input_order));
        }

        // Step 6: Load and embed sections if configured
        if !self.config.embedded_files.is_empty() {
            info!(
                "Embedding {} sections in bundle",
                self.config.embedded_files.len()
            );
            for file_config in &self.config.embedded_files {
                debug!("Loading section from {:?}", file_config.path);
                let (section_id, content_type, data) =
                    self.load_section_data_with_base_path(file_config, base_path)?;
                writer.add_raw_section(&section_id, &content_type, data);
                debug!("Added section '{}'", section_id);
            }
        }

        let bundle = writer.finish();

        info!(
            "Compilation to bundle complete: {} bytes total{}",
            bundle.len(),
            if self.config.produces_v2_bundle() {
                " (V2 with sections)"
            } else {
                ""
            }
        );

        Ok(bundle)
    }

    /// Compile ONNX model with layer-wise splitting for memory-efficient inference.
    ///
    /// This method detects transformer layer structure in the model and compiles
    /// each layer as a separate HOLB model, packaged into a single HOLM pipeline.
    ///
    /// # Use Cases
    ///
    /// - Large models (70B+ parameters) that don't fit in memory
    /// - Memory-constrained inference environments
    /// - Layer-by-layer execution with weight prefetching
    ///
    /// # Returns
    ///
    /// A HOLM pipeline bundle containing one HOLB model per layer.
    ///
    /// # Errors
    ///
    /// Returns `LayerWiseError` if:
    /// - No transformer layer structure is detected
    /// - Layer extraction fails
    /// - Individual layer compilation fails
    ///
    /// # Example
    ///
    /// ```ignore
    /// let compiler = OnnxCompiler::new();
    /// let onnx_bytes = std::fs::read("llama-70b.onnx")?;
    /// let pipeline_bytes = compiler.compile_layer_wise(&onnx_bytes)?;
    /// std::fs::write("llama-70b.holo", pipeline_bytes)?;
    /// ```
    pub fn compile_layer_wise(&self, onnx_bytes: &[u8]) -> Result<Vec<u8>> {
        use core::{PipelineBundleWriter, layer_detection, layer_splitter};
        use tracing::{debug, info};

        info!("Starting layer-wise ONNX compilation");

        // Step 1: Parse and validate ONNX model
        debug!("Parsing ONNX protobuf");
        let model = parse_model(onnx_bytes)?;
        validate_model(&model)?;
        let opset_version = core::extract_opset_version(&model);
        info!("ONNX opset version: {}", opset_version);

        // Get the graph
        let graph = model
            .graph
            .as_ref()
            .ok_or_else(|| OnnxError::InvalidModel("Model has no graph".into()))?;

        // Step 2: Detect transformer layer structure
        debug!("Detecting transformer layers");
        let layers = layer_detection::detect_transformer_layers(graph).ok_or_else(|| {
            OnnxError::LayerWiseError(
                "No transformer layer structure detected. Model may not be a transformer.".into(),
            )
        })?;

        info!(
            "Detected {} transformer layers (prefix: {})",
            layers.len(),
            layers
                .first()
                .map(|l| l.prefix.as_str())
                .unwrap_or("unknown")
        );

        // Step 3: Split into per-layer models
        debug!("Splitting model by layers");
        let layer_models = layer_splitter::split_by_layers(&model, &layers).map_err(|e| {
            OnnxError::LayerWiseError(format!("Failed to split model by layers: {}", e))
        })?;

        // Step 4: Compile each layer to HOLB
        let mut pipeline_writer = PipelineBundleWriter::new();

        for (layer_name, layer_model) in layer_models {
            info!("Compiling layer: {}", layer_name);

            // Compile this layer's graph to BackendPlan
            let layer_graph = layer_model
                .graph
                .as_ref()
                .ok_or_else(|| OnnxError::LayerWiseError("Layer has no graph".into()))?;

            debug!("Translating layer {} to IR", layer_name);
            let ir_func = if self.config.enable_parallel_execution {
                translate_graph_to_ir_with_groups(layer_graph)?
            } else {
                translate_graph_to_ir(layer_graph)?
            };

            debug!("Compiling layer {} to BackendPlan", layer_name);
            let backend_type = hologram::BackendType::Cpu;
            let (mut plan, header) = self
                .compile_ir_with_config(&ir_func, backend_type)
                .map_err(|e| match e {
                    OnnxError::MemoryBudgetExceeded { .. } => e,
                    _ => OnnxError::IrTranslationError(format!(
                        "Failed to compile layer {}: {}",
                        layer_name, e
                    )),
                })?;
            repair_output_metadata_from_graph(&mut plan, layer_graph);

            // Capture weights (they'll be embedded in the HOLB).
            let weight_bytes = plan.constant_data.clone();
            debug!(
                "Layer {} has {} bytes of weights",
                layer_name,
                weight_bytes.len()
            );

            // Serialize plan to HOLP format
            let graph_bytes = serialize_backend_plan_with_header(&plan, &header)?;

            // Create unified bundle for this layer
            let mut layer_writer = core::UnifiedBundleWriter::new();
            layer_writer.set_graph_bytes(graph_bytes);
            layer_writer.set_weights_bytes(weight_bytes);
            layer_writer.add_section(core::sections::LayerHeaderSection {
                header: header.clone(),
            });
            let input_order = Self::external_input_order(layer_graph);
            if !input_order.is_empty() {
                layer_writer.add_section(core::sections::InputOrderSection::new(input_order));
            }
            let holb_bytes = layer_writer.finish();

            // Add to pipeline
            pipeline_writer.add_model(&layer_name, holb_bytes)?;
        }

        // Step 5: Build the pipeline bundle
        let pipeline_bytes = pipeline_writer.finish();

        info!(
            "Layer-wise compilation complete: {} layers, {} bytes total",
            layers.len(),
            pipeline_bytes.len()
        );

        Ok(pipeline_bytes)
    }

    /// Collect external input names in ONNX order (excluding initializers).
    fn external_input_order(graph: &proto::GraphProto) -> Vec<String> {
        let initializer_names: std::collections::HashSet<&str> = graph
            .initializer
            .iter()
            .map(|init| init.name.as_str())
            .collect();

        let mut inputs: Vec<String> = graph
            .input
            .iter()
            .filter(|input| !initializer_names.contains(input.name.as_str()))
            .map(|input| input.name.clone())
            .collect();

        inputs.sort();
        inputs
    }

    fn compile_ir_with_config(
        &self,
        ir_func: &hologram::OperationGraph,
        backend_type: hologram::BackendType,
    ) -> Result<(
        hologram::backend::BackendPlan,
        hologram::compiler::format::LayerHeaderData,
    )> {
        let pipeline_config = build_hologram_pipeline_config(&self.config)?;
        let result = if let Some(config) = pipeline_config {
            hologram::compiler::compile_ir_with_header_config(ir_func, backend_type, config)
        } else {
            hologram::compiler::compile_ir_with_header(ir_func, backend_type)
        };
        result.map_err(|err| map_hologram_compile_error(err, self.config.memory_budget))
    }

    /// Load section data from a file configuration.
    ///
    /// Returns (section_id, content_type, data) tuple.
    fn load_section_data(&self, config: &EmbeddedFileConfig) -> Result<(String, String, Vec<u8>)> {
        self.load_section_data_with_base_path(config, "")
    }

    /// Load section data from a file configuration with a base path.
    ///
    /// Returns (section_id, content_type, data) tuple.
    fn load_section_data_with_base_path(
        &self,
        config: &EmbeddedFileConfig,
        base_path: impl AsRef<std::path::Path>,
    ) -> Result<(String, String, Vec<u8>)> {
        use std::fs;

        let base_path = base_path.as_ref();

        // Resolve the file path
        let file_path = if config.path.is_absolute() {
            config.path.clone()
        } else {
            base_path.join(&config.path)
        };

        // Read the file
        let data = fs::read(&file_path).map_err(|e| {
            OnnxError::InternalError(format!(
                "Failed to read embedded file '{}': {}",
                file_path.display(),
                e
            ))
        })?;

        // Get section ID
        let section_id = config.section_id().to_string();

        // Get content type based on section type
        let content_type = match &config.section_type {
            SectionType::Vocabulary => "text/plain".to_string(),
            SectionType::VocabularyJson => "application/json".to_string(),
            SectionType::TokenizerConfig => "application/json".to_string(),
            SectionType::ModelConfig => "application/json".to_string(),
            SectionType::SpecialTokensMap => "application/json".to_string(),
            SectionType::PreprocessorConfig => "application/json".to_string(),
            SectionType::GenerationConfig => "application/json".to_string(),
            SectionType::SentencePiece => "application/x-sentencepiece".to_string(),
            SectionType::Raw { content_type } => content_type.clone(),
        };

        Ok((section_id, content_type, data))
    }
}

fn build_hologram_pipeline_config(
    config: &core::OnnxConfig,
) -> Result<Option<hologram::compiler::pipeline::PipelineConfig>> {
    let budget_mb = match config.memory_budget {
        Some(budget_mb) => budget_mb,
        None => return Ok(None),
    };

    let bytes = budget_mb
        .checked_mul(1024 * 1024)
        .ok_or_else(|| OnnxError::InternalError("memory_budget too large".into()))?;

    Ok(Some(
        hologram::compiler::pipeline::PipelineConfig::default().with_low_memory(Some(bytes)),
    ))
}

fn map_hologram_compile_error(
    err: hologram::compiler::api::HoloPlanError,
    memory_budget_mb: Option<usize>,
) -> OnnxError {
    use hologram::compiler::pipeline::CompilationError;

    match err {
        hologram::compiler::api::HoloPlanError::Compilation(
            CompilationError::MemoryLimitError(message),
        ) => {
            if let Some((used_bytes, budget_bytes)) = parse_constant_budget_bytes(&message) {
                return OnnxError::memory_budget_exceeded(
                    bytes_to_mb(used_bytes),
                    bytes_to_mb(budget_bytes),
                );
            }
            if let Some(budget_mb) = memory_budget_mb {
                return OnnxError::memory_budget_exceeded(0, budget_mb);
            }
            OnnxError::HologramError(format!("Memory limit exceeded: {}", message))
        }
        _ => OnnxError::HologramError(format!("Failed to compile IR: {}", err)),
    }
}

fn parse_constant_budget_bytes(message: &str) -> Option<(usize, usize)> {
    let mut used_bytes = None;
    let mut budget_bytes = None;

    let mut iter = message.split_whitespace();
    while let Some(word) = iter.next() {
        if word == "bytes" {
            used_bytes = iter.next().and_then(|value| value.parse::<usize>().ok());
        }
        if word == "budget" {
            budget_bytes = iter.next().and_then(|value| value.parse::<usize>().ok());
        }
    }

    match (used_bytes, budget_bytes) {
        (Some(used), Some(budget)) => Some((used, budget)),
        _ => None,
    }
}

fn bytes_to_mb(bytes: usize) -> usize {
    let mb = 1024 * 1024;
    bytes.div_ceil(mb)
}

